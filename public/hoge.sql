WITH T AS (
  SELECT
    KJNO
  , HOMON_ARG_NO
  , CASE WHEN M.SD_NM ||'：'|| NVL(S.SD_CHARGE,0) ||'円' AS SDCOD
  , ROW_NUMBER() OVER (PARTITION BY KJNO, S.SD_COD ORDER BY S.SD_FR_BI DESC) AS RN
  FROM (
    SELECT KJNO, HOMON_ARG_NO, SD_COD FROM HMN_PCDAT S1
    WHERE HOMON_WSH_BI BETWEEN 20260715 AND 20260716
      AND SPID = '08012345678'
      AND HOMON_ARG_NO = (SELECT MAX(HOMON_ARG_NO) FROM HMN_PCDAT S2 WHERE S2.KJNO = S1.KJNO)
  ) P
  INNER JOIN HMN_SDHIS S ON S.SD_COD = P.SD_COD AND S.SD_FR_BI <= P.HOMON_ARG_BI
  INNER JOIN HMN_SDMST M ON M.SD_COD = P.SD_COD
)
SELECT KJNO, HOMON_ARG_NO, SDCOD FROM T WHERE RN = 1;
